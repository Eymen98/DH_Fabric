const pageKey = document.body.dataset.page;
const langButtons = document.querySelectorAll(".lang-btn");

function applyPolicyLanguage(lang) {
  const safeLang = policyTranslations[pageKey]?.[lang] ? lang : "ar";
  const content = policyTranslations[pageKey][safeLang];

  document.documentElement.lang = safeLang;
  document.body.setAttribute("lang", safeLang);
  document.documentElement.dir = safeLang === "ar" ? "rtl" : "ltr";

  document.querySelector("[data-policy='title']").textContent = content.title;
  document.querySelector("[data-policy='intro']").textContent = content.intro;
  document.title = `${content.title} | DH Fabrics`;

  const sectionsRoot = document.getElementById("policySections");
  sectionsRoot.innerHTML = content.sections
    .map(
      (section) => `
        <section class="policy-section">
          <h2>${section.heading}</h2>
          <p>${section.body}</p>
        </section>
      `
    )
    .join("");

  document.querySelector("[data-back-home]").textContent =
    safeLang === "ar" ? "العودة إلى الرئيسية" : safeLang === "tr" ? "Ana Sayfaya Dön" : "Back to Home";

  document.querySelector("[data-last-updated]").textContent =
    safeLang === "ar" ? "آخر تحديث: أبريل 2026" : safeLang === "tr" ? "Son güncelleme: Nisan 2026" : "Last updated: April 2026";

  langButtons.forEach((btn) => btn.classList.toggle("active", btn.dataset.lang === safeLang));
  localStorage.setItem("siteLanguage", safeLang);
}

langButtons.forEach((btn) => {
  btn.addEventListener("click", () => applyPolicyLanguage(btn.dataset.lang));
});

applyPolicyLanguage(localStorage.getItem("siteLanguage") || "ar");
